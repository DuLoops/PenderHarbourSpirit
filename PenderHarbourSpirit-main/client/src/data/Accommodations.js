export const getAccomodationsByType = (type) => {
    var queryResults = [];

    accommodations.forEach(accommodation => {
        if(accommodation.type === type) {
            queryResults.push(accommodation);
        }
    });

    queryResults.sort(titleSort);

    return queryResults; //An array
}

